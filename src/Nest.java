
public class Nest {
    private Integer s;
    private Integer ac;
    private Integer acc;
    private String  m;
    private String  a;
    private String  aa;

    public Nest(){

    }
    public Nest(Integer s,Integer ac,Integer acc,String  m,String  a,String  aa){
        this.s=s;
        this.ac=ac;
        this.acc=acc;
        this.m=m;
        this.a=a;
        this.aa=aa;
    }

    public Integer getS() {
        return s;
    }

    public void setS(Integer s) {
        this.s = s;
    }

    public Integer getAc() {
        return ac;
    }

    public void setAc(Integer ac) {
        this.ac = ac;
    }

    public Integer getAcc() {
        return acc;
    }

    public void setAcc(Integer acc) {
        this.acc = acc;
    }

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getAa() {
        return aa;
    }

    public void setAa(String aa) {
        this.aa = aa;
    }
}
